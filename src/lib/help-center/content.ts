export function getHelpIndex(locale: Locale) {
  return HELP_INDEXES[locale] ?? null;
}

export function getHelpPages(locale: Locale) {
  return HELP_PAGES[locale] ?? [];
}

export function getHelpPage(locale: Locale, slug: string) {
  return getHelpPages(locale).find((page) => page.slug === slug) ?? null;
}

const HELP_INDEXES: Partial<Record<Locale, HelpIndex>> = {
  [defaultLocale]: HELP_INDEX,
  de: HELP_INDEX_DE,
  en: HELP_INDEX_EN,
  es: HELP_INDEX_ES,
  fr: HELP_INDEX_FR,
  pt: HELP_INDEX_PT,
};

const HELP_PAGES: Partial<Record<Locale, HelpPage[]>> = {
  [defaultLocale]: HELP_PAGES_JA,
  de: HELP_PAGES_DE,
  en: HELP_PAGES_EN,
  es: HELP_PAGES_ES,
  fr: HELP_PAGES_FR,
  pt: HELP_PAGES_PT,
};

export function getHelpLocales() {
  return (Object.keys(HELP_PAGES) as Locale[]).filter((locale) => {
    const helpIndex = HELP_INDEXES[locale];
    const helpPages = HELP_PAGES[locale];
    return Boolean(helpIndex && helpPages && helpPages.length > 0);
  });
}

export function hasHelpContent(locale: Locale) {
  return getHelpLocales().includes(locale);
}
